"""
Тариф за електроенергію нараховується наступним чином:
За А кіловат - В грн, все, що вище норми - по тарифу С.
Порахуйте, яку суму потрібно сплатити, якщо використано
Т кіловат.
Дано:
А,В,С,Т
Вивести суму до сплати
30 2 3 30 - 60
20 1 3 30 - 50
30 2 3 20 - 40
"""
A, B, C, T = map(int, input().split())
print(A*B + (T-A)*C if T > A else T*B)
if T > A:
    print(A*B + (T-A)*C)
else:
    print(T*B)

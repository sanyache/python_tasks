"""
Хлопчик будує сходи із кубиків. На кожну наступну сходинку йде на 1 кубик більше
Визначити висоту останньої сходинки, якщо відомо загальну кількість
використаних кубиків
1 1
4 2
6 3
"""
import math
k = int(input())
h = 1
s = 1
while s < k:
    h += 1
    s += h
print(h if s == k else h-1)
print(int((-1 + math.sqrt(1 + 8 * k))/2))


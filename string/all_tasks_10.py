"""
Вчитель ставить + , якщо студент здав практичну роботу, - якщо не здав.
Залікова виставляється студенту, якщо здані всі роботи. Перевірте
чи студент допускається до здачі заліку
Дано: набір знаків без пробілів
Вивести: YES або NO
"""
tasks = input()
for task in tasks:
    if task == '-':
        print("NO")
        break
else:
    print("YES")

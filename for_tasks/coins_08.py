"""
На столі лежать монетки. Деякі лежать вверх гербом, інші решкою.
Визначте мінімальне число монеток, які потрібно перевернути,
щоб всі монетки були повернуті вверх однією і тією ж стороною
Дано: список монет, 1 - решка, 0- герб
Вивести: мінімальна кількість монет
"""

coins = list(map(int, input().split()))
upper = 0
for coin in coins:
    if coin == 1:
        upper += 1
if len(coins) - upper > upper:
    print(upper)
else:
    print(len(coins)-upper)